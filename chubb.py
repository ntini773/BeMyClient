# Ensure y is a 1D numpy int array
if isinstance(y, (pd.Series, pd.DataFrame)):
    y = np.asarray(y).reshape(-1)
else:
    y = np.asarray(y).reshape(-1)
y = y.astype(int)

# Keep feature names before converting X to numpy
feature_names = list(X.columns) if hasattr(X, "columns") else [f"f{i}" for i in range(X.shape[1])]
X = np.asarray(X).astype(np.float32)

# ----------------------
# Train / val / test split (stratified)
X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.2, random_state=SEED, stratify=y
)
X_train, X_val, y_train, y_val = train_test_split(
    X_train, y_train, test_size=0.25, random_state=SEED, stratify=y_train
)  # 0.25 * 0.8 = 0.2

# ----------------------
# Torch Dataset / DataLoader
class TabularDataset(Dataset):
    def __init__(self, X, y):
        self.X = torch.tensor(X, dtype=torch.float32)
        self.y = torch.tensor(y, dtype=torch.float32).view(-1, 1)
    def __len__(self):
        return len(self.y)
    def __getitem__(self, idx):
        return self.X[idx], self.y[idx]

BATCH_SIZE = 256
train_loader = DataLoader(TabularDataset(X_train, y_train), batch_size=BATCH_SIZE, shuffle=True, drop_last=False)
val_loader   = DataLoader(TabularDataset(X_val,   y_val),   batch_size=BATCH_SIZE, shuffle=False)
test_loader  = DataLoader(TabularDataset(X_test,  y_test),  batch_size=BATCH_SIZE, shuffle=False)

# ----------------------
# MLP model (simple but effective)
class MLP(nn.Module):
    def __init__(self, input_dim, hidden_dims=[256,128,64], dropout=0.3):
        super().__init__()
        layers = []
        d_in = input_dim
        for h in hidden_dims:
            layers.append(nn.Linear(d_in, h))
            layers.append(nn.ReLU())
            layers.append(nn.BatchNorm1d(h))
            layers.append(nn.Dropout(p=dropout))
            d_in = h
        layers.append(nn.Linear(d_in, 1))  # single logit for binary classification
        self.net = nn.Sequential(*layers)
    def forward(self, x):
        return self.net(x)  # logits (no sigmoid)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model = MLP(input_dim=X.shape[1], hidden_dims=[256,128,64], dropout=0.25).to(device)

# ----------------------
# Loss, optimizer, scheduler
criterion = nn.BCEWithLogitsLoss()
optimizer = torch.optim.AdamW(model.parameters(), lr=1e-3, weight_decay=1e-5)
scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.5, patience=4)

# ----------------------
# Training loop with early stopping
EPOCHS = 100
early_stop_patience = 10
best_val_loss = float('inf')
epochs_no_improve = 0
best_state = None

def evaluate(model, loader):
    model.eval()
    ys, probs = [], []
    losses = []
    with torch.no_grad():
        for xb, yb in loader:
            xb = xb.to(device)
            yb = yb.to(device)
            logits = model(xb)                     # logits on device
            prob = torch.sigmoid(logits).cpu().numpy().reshape(-1)
            probs.append(prob)
            ys.append(yb.cpu().numpy().reshape(-1))
            # compute loss per batch using logits (safer numerically)
            loss_batch = criterion(logits, yb).item()
            losses.append(loss_batch)
    y_true = np.concatenate(ys) if len(ys) > 0 else np.array([])
    y_prob = np.concatenate(probs) if len(probs) > 0 else np.array([])
    avg_loss = float(np.mean(losses)) if len(losses) > 0 else float('nan')
    auc = roc_auc_score(y_true, y_prob) if (len(y_true) > 0 and len(np.unique(y_true)) > 1) else float('nan')
    y_pred_label = (y_prob >= 0.5).astype(int)
    acc = accuracy_score(y_true, y_pred_label) if len(y_true) > 0 else float('nan')
    prec = precision_score(y_true, y_pred_label, zero_division=0) if len(y_true) > 0 else float('nan')
    rec = recall_score(y_true, y_pred_label, zero_division=0) if len(y_true) > 0 else float('nan')
    return {"loss": avg_loss, "auc": auc, "acc": acc, "prec": prec, "rec": rec, "y_true": y_true, "y_prob": y_prob}

for epoch in range(1, EPOCHS + 1):
    model.train()
    epoch_losses = []
    for xb, yb in train_loader:
        xb = xb.to(device)
        yb = yb.to(device)
        optimizer.zero_grad()
        logits = model(xb)               # logits
        loss = criterion(logits, yb)     # BCEWithLogits
        loss.backward()
        optimizer.step()
        epoch_losses.append(loss.item())
    train_loss = float(np.mean(epoch_losses)) if len(epoch_losses) > 0 else float('nan')
    val_metrics = evaluate(model, val_loader)
    val_loss = val_metrics["loss"]
    print(f"Epoch {epoch:03d} | train_loss: {train_loss:.4f} | val_loss: {val_loss:.4f} | val_auc: {val_metrics['auc']:.4f} | val_acc: {val_metrics['acc']:.4f}")
    scheduler.step(val_loss)
    # early stopping
    if val_loss < best_val_loss - 1e-6:
        best_val_loss = val_loss
        epochs_no_improve = 0
        best_state = {k:v.cpu().clone() for k,v in model.state_dict().items()}  # save CPU copy
        torch.save(best_state, "best_churn_model.pth")
    else:
        epochs_no_improve += 1
        if epochs_no_improve >= early_stop_patience:
            print(f"Early stopping triggered after {epoch} epochs.")
            break

# Load best model
if best_state is not None:
    model.load_state_dict(best_state)
    model.to(device)

# ----------------------
# Final evaluation on test set
test_metrics = evaluate(model, test_loader)
print("\nTest set results:")
print(f"Test loss: {test_metrics['loss']:.4f}")
print(f"Test AUC: {test_metrics['auc']:.4f}")
print(f"Test Acc: {test_metrics['acc']:.4f}, Precision: {test_metrics['prec']:.4f}, Recall: {test_metrics['rec']:.4f}")

