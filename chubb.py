import matplotlib.pyplot as plt

# ----------------------
# reproducibility
SEED = 42
random.seed(SEED)
np.random.seed(SEED)
torch.manual_seed(SEED)
if torch.cuda.is_available():
    torch.cuda.manual_seed_all(SEED)

# ----------------------
# Assumes X (pd.DataFrame) and y (pd.Series/array) already exist in the environment.
# If you run as standalone, load X,y here (uncomment & adapt):
# df = pd.read_csv("preprocessed.csv")
